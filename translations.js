// Language Detection and Application
function changeLanguage(lang) {
    // Update all translatable elements
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });
    
    // Update placeholder texts
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.getAttribute('data-translate-placeholder');
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });
    
    // Update language display in dropdowns
    document.querySelectorAll('#currentLanguage, #footerLanguage').forEach(el => {
        el.textContent = getLanguageName(lang);
    });
    
    // Set HTML lang attribute
    document.documentElement.lang = lang;
    
    // Save preference to localStorage
    localStorage.setItem('netflix-clone-lang', lang);
}

// Get full language name for display
function getLanguageName(code) {
    const names = {
        'en': 'English',
        'hi': 'हिंदी',
        'ml': 'മലയാളം',
        'de': 'Deutsch',
        'fr': 'Français',
        'zh': '中文'
    };
    return names[code] || 'English';
}

// Initialize language system
document.addEventListener('DOMContentLoaded', () => {
    // Set up language selector buttons
    document.querySelectorAll('.language-selector').forEach(button => {
        button.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = e.target.getAttribute('data-lang');
            changeLanguage(lang);
        });
    });
    
    // Load saved language or default to English
    const savedLang = localStorage.getItem('netflix-clone-lang') || 'en';
    changeLanguage(savedLang);
    
    // Set initial dropdown text
    document.querySelectorAll('#currentLanguage, #footerLanguage').forEach(el => {
        el.textContent = getLanguageName(savedLang);
    });
});

// Make available globally
window.changeLanguage = changeLanguage;